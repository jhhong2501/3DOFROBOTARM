void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
   if(htim-> Instance == TIM4){
      if(cnt[0] == 1000){
         //////Encoder Set//////
         encoder[0] = TIM1->CNT;
         encoder[1] = TIM2->CNT;
         encoder[2] = TIM3->CNT;
         enc_deg[0] = encoder[0]*0.03269;
         enc_deg[1] = encoder[1]*0.03269;
         enc_deg[2] = encoder[2]*0.0498063;

         //////Control Section///////
         DC_Output(Current_Control(0, Speed_Control(0, Pos_Control(0, endeffector[0]))));
         DC_Output(Current_Control(0, Speed_Control(0, Pos_Control(0, endeffector[1]))));
         DC_Output(Current_Control(0, Speed_Control(0, Pos_Control(0, endeffector[2]))));
         cnt[0] = 0;
      }
      cnt[0]++;
   }
}

float Pos_Control(int i, float td){
	err_deg[i] = td - cur_deg[i];
	targetomega = Kp_pos[i] * err_deg[i] + Kd_pos[i] * (err_deg[i] - pre_err_deg[i]) * 2;
	return targetomega;
}

float Speed_Control(int j, float to){
	tar_vel[j] = to;
	cur_vel[j] = 0;
	err_vel[j] = tar_vel[j] - cur_vel[j];
	errsum_vel[j] += err_vel[j];

	targetcurrent = Kp_vel[j] * err_vel[j] + Ki_vel[j] * errsum_vel[j] *0.05;
	return targetcurrent;
}

float Current_Control(int k, float tc){
	tar_cur[k] = tc;
	cur_cur[k] = 0;
	err_cur[k] = tar_cur[k] - cur_cur[k];
	errsum_cur[k] += err_cur[k];

	targetvoltage = Kp_cur[k] * err_cur[k] + Ki_cur[k] * errsum_cur[k] *0.05;
	return targetvoltage;
}

float MAF(float maf_input){
   maf_average = maf_average + (maf_input - maf_data[0])/20;
   for (maf_MAX = 0; maf_MAX < 20 - 1; maf_MAX++){
	   maf_data[maf_MAX] = maf_data[maf_MAX+1];
	   maf_data[maf_MAX] = maf_input;
   }

   return maf_average;
}

void DC_Output(float tv){
	if (tv >= 0.0)
		HAL_GPIO_WritePin(dir_y_GPIO_Port, dir_y_Pin, 1);
	else{
		HAL_GPIO_WritePin(dir_y_GPIO_Port, dir_y_Pin, 0);
		tv *= -1.0;
	}
   TIM4->CCR1 = (tv/12)*(100.0-1);
}

int _write(int fd, char *str, int len){
   for(int i=0; i<len; i++){
      HAL_UART_Transmit(&huart2, (uint8_t *)&str[i], 1, 0xFFFF);
   }
   return len;
}

